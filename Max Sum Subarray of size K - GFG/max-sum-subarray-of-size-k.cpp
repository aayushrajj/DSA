class Solution{   
public:
    int maximumSumSubarray(int K, vector<int> &Arr , int N){
        int res=0;
        int i=0;
        while(i<K){
            res += Arr[i];
            i++;
        }
        int currsum = res;
        while(i<N){
            currsum += Arr[i] - Arr[i-K];
            res = max(res,currsum);
            i++;
        }
        return res;
    }
};

